public class Tecnos {
    private int idTecno;
    private String nomTecno;
    private String nomCortoTecno;
    private String logoTecno;

    public int getIdTecno() {
        return idTecno;
    }

    public void setIdTecno(int idTecno) {
        this.idTecno = idTecno;
    }

    public String getNomTecno() {
        return nomTecno;
    }

    public void setNomTecno(String nomTecno) {
        this.nomTecno = nomTecno;
    }

    public String getNomCortoTecno() {
        return nomCortoTecno;
    }

    public void setNomCortoTecno(String nomCortoTecno) {
        this.nomCortoTecno = nomCortoTecno;
    }

    public String getLogoTecno() {
        return logoTecno;
    }

    public void setLogoTecno(String logoTecno) {
        this.logoTecno = logoTecno;
    }
}
